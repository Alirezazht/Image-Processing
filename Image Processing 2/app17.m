clc;clear;close all;
g1 = imread('pillsetc.png');
g2 = g1(118:221,76:174);
g3 = g1(221:280,312:390);
g4 = g1(294:358,221:295);
hsizex= [1 1 1;
        0  0  0;
        -1 -1 -1];
hsizey=[-1 0 1;
        -2 0 2
        -1 0 1];;
Ix1=imfilter(g2,hsizex);
Iy1=imfilter(g2,hsizey);
Ix2=imfilter(g3,hsizex);
Iy2=imfilter(g3,hsizey);
Ix3=imfilter(g4,hsizex);
Iy3=imfilter(g4,hsizey);
It1=abs(Ix1)+abs(Iy1);
It2=abs(Ix2)+abs(Iy2);
It3=abs(Ix3)+abs(Iy3);
subplot(3,3,1);plot(Ix1(52,:));
title('panjare1 Gx');
subplot(3,3,2);plot(Iy1(52,:));
title('panjare1 Gy');
subplot(3,3,3);plot(It1(52,:));
title('panjare1 Gx+Gy');
subplot(3,3,4);plot(Ix2(30,:));
title('panjare2 Gx');
subplot(3,3,5);plot(Iy2(30,:));
title('panjare2 Gy');
subplot(3,3,6);plot(It2(30,:));
title('panjare2 Gx+Gy');
subplot(3,3,7);plot(Ix3(32,:));
title('panjare3 Gx');
subplot(3,3,8);plot(Iy3(32,:));
title('panjare3 Gy');
subplot(3,3,9);plot(It3(32,:));
title('panjare3 Gx++Gy');
